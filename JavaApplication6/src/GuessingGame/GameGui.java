    private void winStatus() {
        JOptionPane.showMessageDialog(null, "You guessed the number correctly", "you win", JOptionPane.INFORMATION_MESSAGE);
        int ans = JOptionPane.showConfirmDialog(null, "start a new game", "new game??", JOptionPane.YES_NO_CANCEL_OPTION);
        if(ans==0){
            clearFields();
        }
        else this.dispose();
    }
    
    private void outOfLives(){
        JOptionPane.showMessageDialog(null,"no more guesses");
        int ans = JOptionPane.showConfirmDialog(null, "start a new game", "new game??", JOptionPane.YES_NO_CANCEL_OPTION);
        if(ans==0){
            clearFields();
        }
        else this.dispose();
    }

    private void clearFields() {
        txtRange.requestFocus();
        txtGuessBox.setText("");
        txtRange.setText("");
        lblGuessType.setText("");
    }

