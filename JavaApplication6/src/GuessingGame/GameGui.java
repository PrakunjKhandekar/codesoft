    private void txtGuessBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtGuessBoxActionPerformed
        int input=Integer.parseInt(txtGuessBox.getText().trim());
        if(input > range){
            JOptionPane.showMessageDialog(null,"Guess within range","out of range!!",JOptionPane.ERROR_MESSAGE);
        }
        else if (input < value) {
            if(value-input < 10) {lblGuessType.setText("little bit high");}
            else lblGuessType.setText("too low");
        } else if (input > value) {
            if(input-value < 10) {lblGuessType.setText("little bit low");}
            else lblGuessType.setText("Too high");
        } else if (Integer.parseInt(txtGuessBox.getText().trim()) == value) {
            winStatus();
        }
        if(count==2 && input!=value){
            outOfLives();
        }
        ++count;
    }//GEN-LAST:event_txtGuessBoxActionPerformed

    private void btnStartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStartActionPerformed
        try {
            if (txtRange.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please insert a value to start");
                return;
            }
            range = Integer.parseInt(txtRange.getText().trim());
            Random rd = new Random();
            value = rd.nextInt(range);
            System.out.println(value);
        } catch (NumberFormatException n) {
            JOptionPane.showMessageDialog(null, "Please enter numeric data");
            return;
        }
    }//GEN-LAST:event_btnStartActionPerformed

